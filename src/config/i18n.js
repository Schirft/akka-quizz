/**
 * Get translation for a key.
 * Priority: localStorage akka_lang → fallback 'en'
 */
export function t(key, lang) {
  const l = lang || localStorage.getItem('akka_lang') || 'en'
  return translations[l]?.[key] || translations.en[key] || key
}

/**
 * Get translation with parameter substitution.
 * Usage: tp('question_of', lang, { n: 3, total: 5 }) → "Question 3 of 5"
 */
export function tp(key, lang, params = {}) {
  let text = t(key, lang)
  Object.entries(params).forEach(([k, v]) => {
    text = text.replace(`{${k}}`, v)
  })
  return text
}
